                                                                                                        <h2>Procurement
                                                                                                            At UNDP</h2>
                                                                                                        <p>
                                                                                                        <strong>Audience: </strong>
                                                                                                                    

                                                                                            Any company or individual that need to register as a supplier in the UNDP supplier portal to either participate in UNDP tender processes or be issued with contracts or payments.

                                                                                            

                                                                                            UNDP Procurement practitioners and vendor approvers in order to understand the supplier registration process and assist when needed. 

                                                                                            This user guides explains steps that a supplier needs to follow to search for a tender and submit a bid response in the UNDP Quantum Supplier Portal. In particular it covers the following sections: 

                                                                                            

                                                                                            What information is required to register in the portal.
                                                                                            Detailed steps to complete as part of creating a profile in the portal.
                                                                                            How to confirm registration and complete the Multi Factor Authentication (MFA).
                                                                                            Practical tip useful for registration process.
                </p>


                                                                                                        <p> Before starting please to be sure to have prepared the following information and supporting documents ready as they will be required during the registration.

 

  <strong><br> <br> Mandatory: </strong>

       <ul>
             <li>
                <strong>
                </strong>
                D-U-N-S number (if you have one) and<br>
                Taxpayer ID or Tax registration number.<br>
                Company name as legally registered.<br>
                Contact details for maintaining the account.<br>
                Legal address of the company.<br>
                Document about women ownership status of the company if relevant.<br>
                Banking Proof for Spend /Authorised Suppliers.<br>
                Copies for company registration documents.<br>
                Valid email address that you will choose to use for creating and accessing the portal on behalf of the company.<br>
            </li>
             
                
        </ul>
    <strong>
        <br>Recommended:
    </strong>

        <ul>
             <li>
                <strong>
                </strong>
                Brief description of the company profile.<br> 
                Copies of ISO 9001, ISO 14001, ISO 14064, or equivalent.<br> 
                Copies of internal company policy documents relevant to the above quality requirements.<br> 
            </li>
             
                
        </ul>
     
        
        <strong>
            <br>NOTES:
        </strong>

        <ul>
             <li>
             Only Latin characters are allowed for the Company name.<br> 
                Special characters are not permitted in Company names.<br> 
                All the fields marked with a blue Asterix means is mandatory to complete.<br> 
                </li>
             
                
        </ul>

</p>
                                                                                                        
